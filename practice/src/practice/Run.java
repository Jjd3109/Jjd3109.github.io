package practice;

public class Run {
	public static final double PI = 3.14;
	public double radius;
	
	public Run() {}
	
	public Run(double radius) {
		this.radius = radius;
	}
	
	public double getRadius() {
		return radius;
	}
	public void setRadius(double radius) {
		this.radius = radius;
	}
	public static double getPi() {
		return PI;
	}
	
	
	
	
}
